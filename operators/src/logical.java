public class logical {
    public static void main(String[] args) {
       
        System.out.println("Logial AND");
        System.out.println((2>1)&&(7==7)); //true
        System.out.println((4>5)&& (9!=2));

        System.out.println("Logial OR");
        System.out.println((2>1)||(7<=1)); //true
        System.out.println((4>5)|| (9!=2)); 

        
        System.out.println("Logial NOT");
        System.out.println(!(4==4)); //true
        System.out.println(!(4>5)); 
        
    }
}
