public class assignment{
    public static void main(String[] args) {
        int a;
        a=40;
        System.out.println(a); //simple assignment operator

        // compound operator

        System.out.println((a+=10));
        System.out.println((a-=10));
        System.out.println((a*=10));
        System.out.println((a/=10));
        
    }
}