public class nestedforloop {
    public static void main(String[] args) {
        System.out.println("Rectangle pattern: ");
        System.out.println("");
        for(int i=1;i<=10;i++){
            for(int j=1;j<=40;j++){
                System.out.print("*");
            }
            System.out.println("");
        }
    }
    
}
